package user;

public class Client {
	
	private String clientName, clientEmail, clientPassword;
	private int clientID, clientBDay, clientBMonth, clientBYear;
	private int clientType = 0;
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	public String getClientPassword() {
		return clientPassword;
	}
	public void setClientPassword(String clientPassword) {
		this.clientPassword = clientPassword;
	}
	public int getClientType() {
		return clientType;
	}
	public void setClientType(int clientType) {
		this.clientType = clientType;
	}
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getClientEmail() {
		return clientEmail;
	}
	public void setClientEmail(String clientEmail) {
		this.clientEmail = clientEmail;
	}
	public int getClientID() {
		return clientID;
	}
	public void setClientID(int clientID) {
		this.clientID = clientID;
	}
	public int getClientBDay() {
		return clientBDay;
	}
	public void setClientBDay(int clientBDay) {
		this.clientBDay = clientBDay;
	}
	public int getClientBMonth() {
		return clientBMonth;
	}
	public void setClientBMonth(int clientBMonth) {
		this.clientBMonth = clientBMonth;
	}
	public int getClientBYear() {
		return clientBYear;
	}
	public void setClientBYear(int clientBYear) {
		this.clientBYear = clientBYear;
	}
	
	
	
	
	
	
}
